package day12_SwitchCase;

public class Beden_Numaralari_sorusu {
    public static void main(String[] args) {
        int beden=60;
        switch (beden){
            case 38,40,42:
                System.out.println("small");
                break;
            case 44,46,48:
                System.out.println("medium");
                break;
            case 50,52,54:
                System.out.println("large");
                break;
            default:
                System.out.println("yanlış beden numarası");





    }




    }
}
