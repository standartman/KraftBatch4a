package day12_SwitchCase;

public class C06_Switch_Case {
    public static void main(String[] args) {
        int sayi=1;
        switch (sayi) {
            case 1:
                System.out.println("sayı 1 e eşit =  ");
                break;
            case 2:
                System.out.println("sayı 2 e eşit");
                break;
            default:
                System.out.println("sayı eşleşmedi");

        }
    }
}
