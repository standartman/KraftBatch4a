package day5_reviewSecondWeek;

public class RelationalOperators {
    public static void  main(String[] args){
        int a=26;
        int b=25;
        //a b'ye eşittir
        System.out.println("a, b'ye eşit mi?"+(a==b));
        //a b'ye eşit değildir
        System.out.println("a, b'den farklı mı?"+(a!=b));

        // a b'den < küçüktür
        System.out.println("a<b = " + (a < b));

        // a b'den > büyüktür
        System.out.println("a<b = " + (a > b));

        // Girilen 3 basamaklı bir sayının basamaklarına göre tersini bir sayı olarak ekrana yazdırınız









    }


}
