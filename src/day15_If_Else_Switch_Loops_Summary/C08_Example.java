public class C08_Example {
    public static void main(String[] args) {
        //0 ile 20 arasındaki tümsayyıların küpleri alarak ekrana yazdırın

        for (int i=0 ;  i<=20  ;  i+=3){
            System.out.println(i*i*i);
            //sout(Math.pow(i,3)+" ");

        }
    }
}
